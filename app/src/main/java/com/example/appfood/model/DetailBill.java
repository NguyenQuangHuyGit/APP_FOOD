package com.example.appfood.model;

public class DetailBill {
    private int id_bill;
    private int count;
    private int id_food;

    public int getId() {
        return id_bill;
    }

    public void setId(int id) {
        this.id_bill = id;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getId_food() {
        return id_food;
    }

    public void setId_food(int id_food) {
        this.id_food = id_food;
    }
}
